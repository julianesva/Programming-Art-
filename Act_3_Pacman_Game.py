def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


def move():
    """Move pacman and all ghosts."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    move_ghosts()

    update()

    for point, _ in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)
    
"""Function to make more intelligent host. That has the capacity of follow pac-man if pac-man is near to them."""
def move_ghosts():
    """Move ghosts towards Pac-Man if close; otherwise, move randomly."""
    for point, course in ghosts:
        """Calculate distance to Pac-Man"""
        distance_to_pacman = abs(pacman - point)
        """Define the distance threshold"""
        threshold = 100

        if distance_to_pacman < threshold:
            """If Pac-Man is close, attempt to move towards him"""
            direction = vector(0, 0)
            if point.x < pacman.x:
                direction.x = 5
            elif point.x > pacman.x:
                direction.x = -5
            if point.y < pacman.y:
                direction.y = 5
            elif point.y > pacman.y:
                direction.y = -5

            moved = False
            """Attempt to move in the calculated direction"""
            """Try up to 4 random directions if the initial one is blocked"""
            for _ in range(4):  
                if valid(point + direction):
                    point.move(direction)
                    course.x = direction.x
                    course.y = direction.y
                    moved = True
                    break
                else:
                    """If the chosen direction is blocked, choose a random direction"""
                    direction = choice([vector(5, 0), vector(-5, 0), vector(0, 5), vector(0, -5)])
                    
            if not moved:
                """If all attempts fail, continue moving in the current direction"""
                if not valid(point + course):
                    """If current direction is blocked, choose a new random direction"""
                    course.x, course.y = choice([vector(5, 0), vector(-5, 0), vector(0, 5), vector(0, -5)]).x, choice([vector(5, 0), vector(-5, 0), vector(0, 5), vector(0, -5)]).y
                point.move(course)
        else:
            """Move randomly if Pac-Man is not close"""
            direction = choice([vector(5, 0), vector(-5, 0), vector(0, 5), vector(0, -5)])
            if valid(point + direction):
                point.move(direction)
                course.x = direction.x
                course.y = direction.y
            else:
                """If random direction is blocked, keep moving in the current direction"""
                if not valid(point + course):
                    """If current direction is blocked, choose a new random direction"""
                    course.x, course.y = choice([vector(5, 0), vector(-5, 0), vector(0, 5), vector(0, -5)]).x, choice([vector(5, 0), vector(-5, 0), vector(0, 5), vector(0, -5)]).y
                point.move(course)

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')


def change(x, y):
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


